function showTheory() {
	var theory = document.getElementById("theory");
	var background = document.getElementById("background--theory");
	theory.style.display = '';
	background.style.display = '';
}

function hideTheory() {
	var theory = document.getElementById("theory");
	var background = document.getElementById("background--theory");
	theory.style.display = 'none';
	background.style.display = 'none';
}